#include "libft.h"

int		ft_factorial(int nb)
{
	int f;
	int i;

	i = 1;
	f = 1;
	if (nb >= 0 && nb <= 12)
	{
		while (i <= nb)
		{
			f *= i;
			i++;
		}
		return (f);
	}
	else
		return (0);
}
