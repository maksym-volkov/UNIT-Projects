#include "libft.h"

char	*ft_strrchr(const char *string, int symbol)
{
	char	k;
	int		i;

	k = symbol;
	i = 0;
	while (string[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (string[i] == k)
			return ((char *)&string[i]);
		i--;
	}
	return (NULL);
}
