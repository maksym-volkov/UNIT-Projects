#include "libft.h"

void	*ft_memset(void *memptr, int val, size_t len)
{
	unsigned char *ptr;

	ptr = (unsigned char*)memptr;
	while (len)
	{
		*ptr++ = (unsigned char)val;
		len--;
	}
	return (memptr);
}
