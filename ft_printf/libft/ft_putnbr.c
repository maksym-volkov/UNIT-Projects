#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	i = n;
	if (i == -2147483648)
	{
		i = i + 1932735284;
	}
	if (i >= -2147483647 && i <= 2147483647)
	{
		if (i < 0)
		{
			ft_putchar('-');
			i = i * (-1);
		}
		if (i > 9)
		{
			ft_putnbr(i / 10);
		}
		ft_putchar(i % 10 + '0');
	}
	if (n == -2147483648)
	{
		ft_putchar('8');
	}
}
