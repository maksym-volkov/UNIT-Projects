#include "libft.h"

int		ft_atoi(char *str)
{
	int				i;
	int				l;
	unsigned long	k;

	i = 0;
	l = 1;
	k = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == 45 || str[i] == 43)
	{
		if (str[i++] == 45)
			l = -1;
	}
	while (str[i] != '\0' && str[i] >= 48 && str[i] <= 57)
	{
		if ((k >= 922337203685477580) && ((str[i] - '0') > 7) && (l == 1))
			return (-1);
		else if ((k >= 922337203685477580) && ((str[i] - '0') > 8) && (l == -1))
			return (0);
		k = (str[i] - 48) + k * 10;
		i++;
	}
	return (k * l);
}
