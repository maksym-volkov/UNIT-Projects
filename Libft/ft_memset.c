#include "libft.h"

void	*ft_memset(void *destination, int c, size_t n)
{
	char			k;
	unsigned char	*str;

	if (n == 0)
		return (destination);
	str = (unsigned char *)destination;
	k = c;
	while (n > 0)
	{
		*str = k;
		n--;
		str++;
	}
	return (destination);
}
