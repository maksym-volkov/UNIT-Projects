#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	char *dptr;

	dptr = dest;
	while (*src != '\0')
	{
		*dptr++ = *src++;
	}
	*dptr = '\0';
	return (dest);
}
