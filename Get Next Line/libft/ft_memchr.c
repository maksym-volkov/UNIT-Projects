#include "libft.h"

void	*ft_memchr(const void *arr, int c, size_t n)
{
	char	*s;
	char	k;
	size_t	i;

	k = c;
	s = (char *)arr;
	i = 0;
	while (i < n)
	{
		if (s[i] == k)
			return (s + i);
		i++;
	}
	return (0);
}
